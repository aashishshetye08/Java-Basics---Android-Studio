package operators;

public class ArithmeticOperator {

	public static void main(String[] args) {

		double num1 = 6.4, num2 = 3.2, res;
		
		System.out.println("Result : "+(num1 + num2));			//Addition Operator
		System.out.println("Result : "+(num1 - num2));			//SUbtraction Operator
		System.out.println("Result : "+(num1 * num2));			//Multiplication Operator
		System.out.println("Result : "+(num1 / num2));			//Division Operator
		System.out.println("Result : "+(num1 % num2));			//Remainder/MOdulus Operator
	}

}
