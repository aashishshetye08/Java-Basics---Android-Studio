package getStarted;

public class DataTypes {

	public static void main(String[] args) {

		boolean a = true;
		char b = 'A';
		byte c = 50;	//-127 to 128
		short d = 100;	//-32758 to 32758 (~)
		int e = 1000;	// -2^31 to 2^31-1
		long f = 85454861225L;		//where L is the suffix
		float g = 2.1879641216F;	//single precision
		double h = 2.315648211218;	//double precision
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
	}

}
